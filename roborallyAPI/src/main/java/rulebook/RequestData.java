package rulebook;

public class RequestData {
	private String color;
	private String item;
	private String owner;
	private String temp;

	public String getItem() {
		return item;
	}

	public String getColor() {
		return color;
	}

	public String getOwner() {
		return owner;
	}
	
	public String getTemp() {
		return temp;
	}
}
